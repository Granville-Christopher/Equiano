const mongoose = require('mongoose');

// Admin schema definition
const adminSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// Create the Admin model from the schema
const Admin = mongoose.model('Admin', adminSchema);

module.exports = Admin;
